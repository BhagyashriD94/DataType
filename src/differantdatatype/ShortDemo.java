package differantdatatype;

public class ShortDemo {
	public static void main(String[] args) {
		short a1=129;
		System.out.println(a1);
		// range-32768 to +32767
		short a2=3000;
		System.out.println(a2);
		short a3=-32768;
		System.out.println(a3);
		short a4=32767;
		System.out.println(a4);
	}

}
